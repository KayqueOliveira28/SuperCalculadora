    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // variaveis com processamento
        int n = Integer.parseInt(txtValor.getText());
        int res  = n % 2;
        double elec = (double) Math.pow(n, 3);
        double rquad = (double) Math.sqrt(n);
        double rcub = (double) Math.cbrt(n);
        int abs = Math.abs(n);
        
        //Saida
        lblRes.setText(Integer.toString(res));
        lblCub.setText(Double.toString(elec));
        lblQuad.setText(String.format("%.2f", rquad));
        lblRCub.setText(String.format("%.2f", rcub));
        lblAbs.setText(Integer.toString(abs));
        
    }//GEN-LAST:event_btnCalcActionPerformed

